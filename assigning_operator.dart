  // =  

  int number =10;
  print(number);// 10

  //+=

  int number1 = 20;
    number1+=20; // number1 = number1 + 20
    print(number1);// 40

  //-=

  int number2=30;
    number2-=10; // number2 = number2 - 10
    print(number2);//20
  
  //*=

  int number3=5;
   number3*=5; // number = number * 5
   print(number3);//25

  // /=

  double number4 = 10;
   number4/=3;// number4 = number4 / 3
   print(number4.toStringAsFixed(2));// 3.33

  // ~/=

  int number5 = 10;
   number5 ~/=4;// number5 = number5 ~/ 4
   print(number5);// 2

  
  double number6 = 120.34;
