void main(List<String> args) {
  // In dart everything is an objects. and we dont need to define the exact datatype dart will read it as the type we added.
  //variables
  String a = 'Ajmal';
  print(a);
  final b='Ajmal';
  print(b);
  const c='Ajmal';
  print(c);
  int d=10;
  print(d);
  num f=20;// also can add point values
  print(f);
  double e = 20.25;// also can add integer value;
  print(e);
  bool g=true;//or  false;
  print(g);
  var h='Ajmal';//can add all type of datas above;
  print(h);

}